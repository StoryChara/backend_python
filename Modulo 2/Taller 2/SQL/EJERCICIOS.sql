SELECT * FROM TABLAS.GUARDERIAS G 
JOIN TABLAS.CUIDADOR C ON G.ID_Guarderia = C.ID_GUARDERIA
JOIN TABLAS.PERRO P ON G.ID_Guarderia = P.ID_GUARDERIA
WHERE G.Nombre = "La Favorita";