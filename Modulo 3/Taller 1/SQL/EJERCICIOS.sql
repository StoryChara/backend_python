-- Punto 2 | Los perros de Mario
SET SQL_SAFE_UPDATES = 0;
SELECT ID INTO @ID_Mario FROM TABLAS.CUIDADORES WHERE Nombre = 'Mario';
UPDATE TABLAS.PERROS SET ID_CUIDADOR = @ID_Mario WHERE Peso < 3;
SELECT * FROM TABLAS.PERROS WHERE ID_CUIDADOR = @ID_Mario;

